    private void addRoomButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRoomButtonActionPerformed
        // TODO add your handling code here:
        int Seats = Integer.parseInt(addSeats.getText());
        String Name = addRoom.getText();
        RoomQueries.addRoom(Name, Seats);
        addRoomLabel.setText("Room has been added to the list");
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getAllWaitlist();
        
        for (int i = 0; i < waitlist.size(); i++) {
            
            String faculty = waitlist.get(i).getFaculty();
            Date date1 = waitlist.get(i).getDate();
            int seats = waitlist.get(i).getSeats();
            boolean result = ReservationQueries.addReservationEntry(faculty, date1, seats);
            
            if (result== true) {
                
                WaitlistQueries.cancelWaitlistEntry(faculty, date1);
                addRoomLabel.setText(faculty+" has been moved to the reservation list");
                
            }
        }
        
        rebuildWaitlist();
        addRoom.setText("");
        addSeats.setText("");
        
    }//GEN-LAST:event_addRoomButtonActionPerformed

