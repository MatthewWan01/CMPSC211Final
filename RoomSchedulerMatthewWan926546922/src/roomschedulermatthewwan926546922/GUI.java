    public void rebuildWaitlist() {
        
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getAllWaitlist();
        WaitlistList.removeAllElements();
        
        for (int i = 0; i < waitlist.size(); i++) {
            
            WaitlistList.addElement("Faculty: " + waitlist.get(i).getFaculty() + "    Date: " + waitlist.get(i).getDate() + "   Seats: " + waitlist.get(i).getSeats());
            
        }  
        
    }
    
    public void rebuildReservationlist() {
        

        
        Date date = Date.valueOf(reservationComboBox.getSelectedItem().toString());
        ArrayList<ReservationEntry> reservation = ReservationQueries.getReservationsByDate(date);
        ReservationList.removeAllElements();
        
        for (int i = 0; i < reservation.size(); i++) {
            
            ReservationList.addElement("Faculty: " + reservation.get(i).getFaculty() + "  Room: " + reservation.get(i).getRoom() + "  Date: " + reservation.get(i).getDate());
            
        }
        
    }
    
