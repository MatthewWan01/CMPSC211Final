    private void reserveSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reserveSubmitButtonActionPerformed
        // TODO add your handling code here:
        String faculty = (String) reserveFacultyComboBox.getSelectedItem();
        int seats = Integer.parseInt(reserveSeatTextField.getText().trim());
        Date date = Date.valueOf(reserveDateComboBox.getSelectedItem().toString());
        boolean result = ReservationQueries.addReservationEntry(faculty, date, seats);

        if (result == false) {

            reserveResult.setText("Reservation could not made, added to Waitlist");
            WaitlistQueries.addWaitlistEntry(faculty, date, seats);

        }

        else {

            reserveResult.setText("Reservation has been made");

        }

        reserveSeatTextField.setText("");

    }//GEN-LAST:event_reserveSubmitButtonActionPerformed

