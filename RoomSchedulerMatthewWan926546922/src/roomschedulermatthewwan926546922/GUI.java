    private void cancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelButtonActionPerformed
        // TODO add your handling code here:
        String faculty = cancelFaculty.getSelectedItem().toString();
        Date date = Date.valueOf(cancelDate.getSelectedItem().toString());
        WaitlistQueries.cancelWaitlistEntry(faculty, date);
        ReservationQueries.cancelReservation(faculty, date);
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getAllWaitlist();
        
        for (int i = 0; i < waitlist.size(); i++) {
            
            String faculty1 = waitlist.get(i).getFaculty();
            Date date1 = waitlist.get(i).getDate();
            int seats1 = waitlist.get(i).getSeats();
            boolean result = ReservationQueries.addReservationEntry(faculty1, date1, seats1);
            
            if (result== true) {
                
                WaitlistQueries.cancelWaitlistEntry(faculty1, date1);
                cancelReservationLabel.setText(faculty1+" has been moved to the reservation list");
                
            }
            
        }
        
        rebuildWaitlist();
        
    }//GEN-LAST:event_cancelButtonActionPerformed

    private void dropButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropButtonActionPerformed
        // TODO add your handling code here:
        String room = dropRoomTextField.getText();
        ArrayList<ReservationEntry> reservation = ReservationQueries.getReservationsByRoom(room);
        
        for (int i = 0; i < reservation.size(); i++) {
            
            String faculty = reservation.get(i).getFaculty();
            Date date = reservation.get(i).getDate();
            int seats = reservation.get(i).getSeats();
            WaitlistQueries.addWaitlistEntry(faculty, date, seats);
            
        }
        
        ReservationQueries.deleteReservation(room);
        RoomQueries.dropRoom(room);
        ArrayList<WaitlistEntry> waitlist = WaitlistQueries.getAllWaitlist();
        
        for (int j = 0; j < waitlist.size(); j++) {
            
            String faculty1 = waitlist.get(j).getFaculty();
            Date date1 = waitlist.get(j).getDate();
            int seats1 = waitlist.get(j).getSeats();
            boolean result = ReservationQueries.addReservationEntry(faculty1, date1, seats1);
            
            if (result== true) {
                WaitlistQueries.cancelWaitlistEntry(faculty1, date1);
                
            }
            else {
                
                rebuildWaitlist();
                
            }
        }
    }//GEN-LAST:event_dropButtonActionPerformed

    private void statusFacultyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_statusFacultyButtonActionPerformed
        // TODO add your handling code here:
        String faculty = statusFacultyCombo.getSelectedItem().toString();
        ArrayList<ReservationEntry> reservations = ReservationQueries.getReservationsByFaculty(faculty);
        ArrayList<WaitlistEntry> waitlists = WaitlistQueries.getWaitlistByFaculty(faculty);
        for (int i = reservations.size() - 1; i > -1; i--) {
            
            StatusFacultyList.addElement("Room: " + reservations.get(i).getRoom() + "    Date: " + reservations.get(i).getDate());
            
        }
        
        for (int j = waitlists.size() - 1; j > -1; j--) {
            
            StatusFacultyList.addElement("Seats: " + waitlists.get(j).getSeats() + "    Date: " + waitlists.get(j).getDate());
            
        }
        
    }//GEN-LAST:event_statusFacultyButtonActionPerformed

